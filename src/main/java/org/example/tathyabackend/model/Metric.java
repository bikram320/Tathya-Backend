package org.example.tathyabackend.model;

public class Metric {

    private int upvote;
    private int downvote;

    public Metric(){

    }

    public int getUpvote() {
        return upvote;
    }

    public void setUpvote(int upvote) {
        this.upvote = upvote;
    }

    public int getDownvote() {
        return downvote;
    }

    public void setDownvote(int downvote) {
        this.downvote = downvote;
    }
}
